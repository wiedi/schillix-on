#pragma weak _munmap = munmap

#include "lint.h"
#include <sys/mman.h>

extern void unregister_locks(caddr_t, size_t);
extern int __munmap(caddr_t, size_t);

int
munmap(caddr_t addr, size_t len)
{
	unregister_locks(addr, len);
	return (__munmap(addr, len));
}
